    {
        // dd($request);
        $validator = $request->validate([

            'user_id' => 'required',
            'nama_member' => 'required',
            'notelp' => 'required|max:14',
            'alamat' => 'required',
            'keluhan' => 'required',
            'jk' => 'required',
        ]);

        $member = Member::create($validator);
         if ($member) {
             return redirect('/indexdatamember');
         }

    }

